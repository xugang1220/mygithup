package cn.yfyue.sysauth.ajax;

public class UserRoleCheckbox {
	private String checkboxId;
	private String checkboxName;
	private String checkboxValue;
	private String checkboxChecked;
	private String orgeFullName;
	public String getOrgeFullName() {
		return orgeFullName;
	}
	public void setOrgeFullName(String orgeFullName) {
		this.orgeFullName = orgeFullName;
	}
	public String getCheckboxId() {
		return checkboxId;
	}
	public void setCheckboxId(String checkboxId) {
		this.checkboxId = checkboxId;
	}
	public String getCheckboxName() {
		return checkboxName;
	}
	public void setCheckboxName(String checkboxName) {
		this.checkboxName = checkboxName;
	}
	public String getCheckboxValue() {
		return checkboxValue;
	}
	public void setCheckboxValue(String checkboxValue) {
		this.checkboxValue = checkboxValue;
	}
	public String getCheckboxChecked() {
		return checkboxChecked;
	}
	public void setCheckboxChecked(String checkboxChecked) {
		this.checkboxChecked = checkboxChecked;
	}
}
